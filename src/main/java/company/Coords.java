package company;

public class Coords {
    public int minX;
    public int maxX;
    public int minY;
    public int maxY;

    public Coords(int minX, int maxX, int minY, int maxY)
    {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }
}
